#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;

	str = (char *)s;
	if (n == 0)
		return ;
	while (--n > 0)
	{
		str[n] = '\0';
	}
	str[n] = '\0';
	s = str;
}
